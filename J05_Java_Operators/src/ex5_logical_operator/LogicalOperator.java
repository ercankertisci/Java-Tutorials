package ex5_logical_operator;

public class LogicalOperator {

	public static void main(String[] args) {
		boolean x = false;
		boolean y = true;

		System.out.println("x&&y=" + (x && y));
		System.out.println("x||y=" + (x || y));
		System.out.println("x^y=" + (x ^ y));
		System.out.println("!x=" + (!x));

	}

}