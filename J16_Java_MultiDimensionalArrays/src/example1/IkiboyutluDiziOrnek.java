package example1;

public class IkiboyutluDiziOrnek {
	public static void main(String[] args) {
		int[][] dizi = { { 1, 7 }, { 5, 9 } };

		System.out.println(dizi[0][0]); // 1. satır 1. sütun
		System.out.println(dizi[0][1]); // 1. satır 2. sütun
		System.out.println(dizi[1][0]); // 2. satır 1. sütun
		System.out.println(dizi[1][1]); // 2. satır 2. sütun
	}
}