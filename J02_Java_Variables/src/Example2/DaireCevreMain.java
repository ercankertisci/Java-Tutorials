package Example2;

public class DaireCevreMain {

	public static void main(String[] args) {

		// Değişkenler
		int yaricap = 7;
		double pi = 3.14159;
		double cevre;

		// Çevreyi hesapla
		cevre = 2 * pi * yaricap;

		// Değişkenleri yazdır
		System.out.println("Yarıçap: " + yaricap);
		System.out.println("Pi: " + pi);
		System.out.println("Dairenin Çevresi: " + cevre);
	}

}
